package Entity;

/**
 *
 * @author Kudo
 */
public class Category {

private String ID;
private String name;
private int status;

    public Category(String ID, String name,int status) {
        this.ID = ID;
        this.name = name;
        this.status = status;
    }

    public Category(String ID, String name) {
        this.ID = ID;
        this.name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }


    
}
